# stdout がそのままセッションのコンテキストに追加される（記憶に依存させない）。
cat <<'EOF'
[商品検索リポジトリ・自動注入ルール]
■ 比較/ランキング/スコアリング系タスク（依頼に「比較・ランキング・おすすめ・TOP◯・
  ベスト・一番・コスパ最強・◯点満点・順位・どれがいい・選ぶなら」を含む）では:
  1. 最初の応答で「比較系タスク」と宣言し、スコアリングより先にブランド地図を作成・提示する
  2. ゲート2(在庫2ソース)・ゲート3(条件充足)をスコアリング前に全候補へ適用する
  3. docs/rules-search-product.md の §4.0/§4.5/§6.0、ルールA〜D を必ず適用する
■ 価格検証の環境制約（重要・実測で確定済み）:
  - Amazon(/dp/) は HTTP 503、楽天(item.rakuten.co.jp) はボット遮断で「ライブ価格取得不可」
  - Yahoo!ショッピング(store.shopping.yahoo.co.jp) はライブ価格取得可能
  - 取得不可チャネルの価格を、公式サイト価格やスニペットやサクラチェッカー価格で
    代用して「そのチャネルの価格」として掲載してはならない（過去に¥3,980 vs 実¥5,280の乖離）
  - 価格は「リンク先チャネルのライブ価格」を出典として明示。非ライブは ⚠️ を必須にする
■ 比較レポート保存時、scripts/check_comparison_report.py が PostToolUse フックで自動実行され、
  価格出典・確認日・除外一覧・件数を機械検証する（違反は exit 2 で差し戻し）
■ 全ての調査・リサーチ・検索タスク共通（網羅性ゲート・docs/rules-research.md）:
  - 着手前に観点マトリクス（6軸: 選択肢空間/評価軸/ユーザー文脈/チャネル・情報源/時間軸/反証視点）を
    作成・提示し、coverage-critic サブエージェントの抜け漏れ監査を受けてから本調査に入る
■ ハルシネーション3原則: ①数値・引用は出典URL+確認日必須（記憶ベースのURL・型番・価格は禁止）
  ②未検証・推定は⚠️+ヘッジ語 ③納品前に fact-check-reviewer の独立レビュー（ゲート4）を必ず通す
  リサーチ系レポートは <!-- report-type: research --> を付与（check_research_report.py が機械検証）
EOF
